local opts = { noremap = true, silent = true }

-- local term_opts = { silent = true }

-- Shorten function name
local keymap = vim.api.nvim_set_keymap

--Remap space as leader key
keymap("", "<Space>", "<Nop>", opts)
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- Modes
--   normal_mode = "n",
--   insert_mode = "i",
--   visual_mode = "v",
--   visual_block_mode = "x",
--   term_mode = "t",
--   command_mode = "c",

-- Disable Arrow Keys
-- We dont need this, force us to use HJKL
keymap("n", "<Up>", "<NOP>", opts)
keymap("n", "<Down>", "<NOP>", opts)
keymap("n", "<Left>", "<NOP>", opts)
keymap("n", "<Right>", "<NOP>", opts)
keymap("v", "<Up>", "<NOP>", opts)
keymap("v", "<Down>", "<NOP>", opts)
keymap("v", "<Left>", "<NOP>", opts)
keymap("v", "<Right>", "<NOP>", opts)

-- Normal --
-- Better window navigation
keymap("n", "<C-h>", "<C-w>h", opts)
keymap("n", "<C-j>", "<C-w>j", opts)
keymap("n", "<C-k>", "<C-w>k", opts)
keymap("n", "<C-l>", "<C-w>l", opts)

-- Resize with arrows
keymap("n", "<C-Up>", ":resize -2<CR>", opts)
keymap("n", "<C-Down>", ":resize +2<CR>", opts)
keymap("n", "<C-Right>", ":vertical resize -2<CR>", opts)
keymap("n", "<C-Left>", ":vertical resize +2<CR>", opts)

-- Navigate buffers
keymap("n", "<S-l>", ":bnext<CR>", opts)
keymap("n", "<S-h>", ":bprevious<CR>", opts)
keymap("n", "<leader>x", ":Bdelete!<CR>", opts)

-- Move text up and down
keymap("n", "<A-j>", "<Esc>:m .+1<CR>==gi", opts)
keymap("n", "<A-k>", "<Esc>:m .-2<CR>==gi", opts)

-- Insert --
-- Press jk fast to exit insert mode
keymap("i", "jk", "<ESC>", opts)
keymap("i", "kj", "<ESC>", opts)

-- Visual --
-- Stay in indent mode
keymap("v", "<", "<gv", opts)
keymap("v", ">", ">gv", opts)

-- Move text up and down
keymap("v", "<A-j>", ":m .+1<CR>==", opts)
keymap("v", "<A-k>", ":m .-2<CR>==", opts)
keymap("v", "p", '"_dP', opts)

-- Visual Block --
-- Move text up and down
keymap("x", "J", ":move '>+1<CR>gv-gv", opts)
keymap("x", "K", ":move '<-2<CR>gv-gv", opts)
keymap("x", "<A-j>", ":move '>+1<CR>gv-gv", opts)
keymap("x", "<A-k>", ":move '<-2<CR>gv-gv", opts)

-- Terminal --
-- Better terminal navigation
-- keymap("t", "<C-h>", "<C-\\><C-N><C-w>h", term_opts)
-- keymap("t", "<C-j>", "<C-\\><C-N><C-w>j", term_opts)
-- keymap("t", "<C-k>", "<C-\\><C-N><C-w>k", term_opts)
-- keymap("t", "<C-l>", "<C-\\><C-N><C-w>l", term_opts)
keymap("n", "<leader>tj", ":lua _JUPYTER_TOGGLE()<CR>", opts)

-- NvimTree --
-- Manage NvimTree
keymap("n", "<leader>e", ":NvimTreeFocus<CR>", opts)
keymap("n", "<C-n>", ":NvimTreeToggle<CR>", opts)

-- Movement --
-- Functionality surrounding in buffer movement and navigation
keymap("n", "<leader>s", ":lua require('leap').leap { target_windows = { vim.fn.win_getid() } }<CR>", opts)

-- Trouble --
-- Toggle Trouble.nvim quickfix list
keymap("n", "<leader>tr", ":TroubleToggle<CR>", opts)

-- Peaches --
-- Trigger peaches commands
keymap("n", "<C-f>", ':silent !tmux split-window "~/.local/bin/peaches projects"<CR>', opts)
keymap("n", "<C-g>", ':silent !tmux split-window "~/.local/bin/peaches ssh"<CR>', opts)

-- Symbols-Outline --
-- Open Right hand Symbols Outline Panel
keymap("n", "<leader>ts", ":SymbolsOutline<CR>", opts)

